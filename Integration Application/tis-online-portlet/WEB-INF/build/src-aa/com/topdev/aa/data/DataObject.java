package com.topdev.aa.data;

import org.apache.struts.validator.ValidatorForm;

public abstract class DataObject extends ValidatorForm
{
	public String ID="0";

	public String KZ_BROWSE="0";

	/**
	 * =1 wenn das Objekt editierbar ist.*/
	public String KZ_EDIT="0";

	public String KZ_DELETE="0";

	/**
	 * =1 wenn es sich bei dem Objekt um einen Download / eine Datei handelt.*/
	public String KZ_DOWNLOAD="0";

	public String KZ_IS_MODIFIED="0";

	public String KZ_IS_DELETED="0";


	public String getID()
	{
		return ID;
	}

	public String getKZ_BROWSE()
	{
		return KZ_BROWSE;
	}

	public String getKZ_DELETE()
	{
		return KZ_DELETE;
	}

	public String getKZ_DOWNLOAD()
	{
		return KZ_DOWNLOAD;
	}

	public String getKZ_EDIT()
	{
		return KZ_EDIT;
	}

	public void setID(String string)
	{
		ID = string;
	}

	public void setKZ_BROWSE(String string)
	{
		KZ_BROWSE = string;
	}

	public void setKZ_DELETE(String string)
	{
		KZ_DELETE = string;
	}

	public void setKZ_DOWNLOAD(String string)
	{
		KZ_DOWNLOAD = string;
	}

	public void setKZ_EDIT(String string)
	{
		KZ_EDIT = string;
	}

	/**
	 * @return Returns the kZ_IS_DELETED.
	 */
	public String getKZ_IS_DELETED()
	{
		return KZ_IS_DELETED;
	}
	/**
	 * @param kz_is_deleted The kZ_IS_DELETED to set.
	 */
	public void setKZ_IS_DELETED(String kz_is_deleted)
	{
		KZ_IS_DELETED = kz_is_deleted;
	}
	/**
	 * @return Returns the kZ_IS_MODIFIED.
	 */
	public String getKZ_IS_MODIFIED()
	{
		return KZ_IS_MODIFIED;
	}
	/**
	 * @param kz_is_modified The kZ_IS_MODIFIED to set.
	 */
	public void setKZ_IS_MODIFIED(String kz_is_modified)
	{
		KZ_IS_MODIFIED = kz_is_modified;
	}
}